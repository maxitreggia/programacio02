for i in (1, 2, 3):
    print(i)
    """
    Returns: 
        1
        2
        3

    """

for i in ("uno", "dos", "tres"):
    print(i)
    """
    Returns:
        uno
        dos
        tres
    """
# La variable i se utiliza para almacenar el valor actual del elemento en
# la secuencia que se está iterando.
# El bucle se ejecuta una vez por cada elemento en la secuencia.
# El valor de i se puede usar dentro del bucle para acceder a diferentes
# elementos de la secuencia.
# El tipo de dato de la variable i depende del tipo de dato de
# la secuencia que se está iterando.
